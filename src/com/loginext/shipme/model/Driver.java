package com.loginext.shipme.model;

public class Driver {
  private String assignDeliveryBoyFlag;
  private String createdByUserId;
  private String id;
  private String updatedByUserId;
  private String clientId;
  private String driverId;
  private String driverName;
  private String phoneNumber;

  public String getAssignDeliveryBoyFlag() {
    return assignDeliveryBoyFlag;
  }
  public void setAssignDeliveryBoyFlag(String assignDeliveryBoyFlag) {
    this.assignDeliveryBoyFlag = assignDeliveryBoyFlag;
  }
  public String getCreatedByUserId() {
    return createdByUserId;
  }
  public void setCreatedByUserId(String createdByUserId) {
    this.createdByUserId = createdByUserId;
  }
  public String getId() {
    return id;
  }
  public void setId(String id) {
    this.id = id;
  }
  public String getUpdatedByUserId() {
    return updatedByUserId;
  }
  public void setUpdatedByUserId(String updatedByUserId) {
    this.updatedByUserId = updatedByUserId;
  }
  public String getClientId() {
    return clientId;
  }
  public void setClientId(String clientId) {
    this.clientId = clientId;
  }
  public String getDriverId() {
    return driverId;
  }
  public void setDriverId(String driverId) {
    this.driverId = driverId;
  }
  public String getDriverName() {
    return driverName;
  }
  public void setDriverName(String driverName) {
    this.driverName = driverName;
  }
  public String getPhoneNumber() {
    return phoneNumber;
  }
  public void setPhoneNumber(String phoneNumber) {
    this.phoneNumber = phoneNumber;
  }
}
