package com.loginext.shipme.model;

public class ConsignmentType {
  private String assignDeliveryBoyFlag;
  private String createdByUserId;
  private String id;
  private String updatedByUserId;
  private String lookupCd;
  private String lookupDesc;
  private String lookupId;
  private String lookupType;

  public String getAssignDeliveryBoyFlag() {
    return assignDeliveryBoyFlag;
  }
  public void setAssignDeliveryBoyFlag(String assignDeliveryBoyFlag) {
    this.assignDeliveryBoyFlag = assignDeliveryBoyFlag;
  }
  public String getCreatedByUserId() {
    return createdByUserId;
  }
  public void setCreatedByUserId(String createdByUserId) {
    this.createdByUserId = createdByUserId;
  }
  public String getId() {
    return id;
  }
  public void setId(String id) {
    this.id = id;
  }
  public String getUpdatedByUserId() {
    return updatedByUserId;
  }
  public void setUpdatedByUserId(String updatedByUserId) {
    this.updatedByUserId = updatedByUserId;
  }
  public String getLookupCd() {
    return lookupCd;
  }
  public void setLookupCd(String lookupCd) {
    this.lookupCd = lookupCd;
  }
  public String getLookupDesc() {
    return lookupDesc;
  }
  public void setLookupDesc(String lookupDesc) {
    this.lookupDesc = lookupDesc;
  }
  public String getLookupId() {
    return lookupId;
  }
  public void setLookupId(String lookupId) {
    this.lookupId = lookupId;
  }
  public String getLookupType() {
    return lookupType;
  }
  public void setLookupType(String lookupType) {
    this.lookupType = lookupType;
  }
}
